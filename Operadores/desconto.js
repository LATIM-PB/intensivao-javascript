// Definindo o preço original do produto
var precoOriginal = 100;

// Definindo o percentual de desconto
var descontoPercentual = 10; // 10%

// Calculando o valor do desconto
var desconto = (precoOriginal * descontoPercentual) / 100;

// Calculando o preço final com desconto
var precoFinal = precoOriginal - desconto;

// Exibindo os resultados
console.log("Preço original: $" + precoOriginal);
console.log("Desconto: $" + desconto);
console.log("Preço final com desconto: $" + precoFinal); 

